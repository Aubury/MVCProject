<li>2. кофемашину и запас кофе на месяц.</li>
<li>3. в самое ближайшее время, у нас появится продуктовая корзина (ежемесячный набор продуктов, бесплатно для пайщиков).</li>
<li>4. один, из первых 50 пайщиков, получит в подарок от кооператива – автомобиль <b>Nissan Almera</b></li>
<li>5. ещё один автомобиль, также абсолютно бесплатно, достанется одному из следующих 50 (+49 первых) пайщиков.</li>
</ul></p>
                <p>В максимально короткие сроки, ПК Мой Дом ГИМ, вольётся во все сферы жизни, чтобы быть в курсе всего, тем самым выбирать области развития кооператива, для удовлетворения нужд наших пайщиков. Мы, все вместе, сообща, будем делать свою жизнь лучше, в наше нелегкое время. Нам всем надо сплотиться, потому что помочь себе, можем только мы сами.</p>
