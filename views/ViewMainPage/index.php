                <p>Мы – группа людей разных возрастов, материального и социального положения, объединённая единым движущим мотивом – создать все необходимые условия, чтобы каждый желающий человек мог сделать свою жизнь лучше, а соответственно превратить весь окружающий нас мир в комфортную, открытую и более доброжелательную для абсолютно каждого человека среду, с неограниченным количеством возможностей для развития.</p>
                <p>Потребительский Кооператив Мой Дом ГИМ – это не какая-то огромная теневая «корпорация зла». Мы, его администрация, - небольшая группа открытых для публики людей, имеющих страницы в популярных соцсетях и готовых общаться и принимать все поступающие идеи и предложения. Мы не имеем за спиной огромного мешка золотых монет, шикарного офиса, друзей миллиардеров или же связей с государством, всё, что у нас есть – это энтузиазм и желание своим собственным честным трудом пробираться к намеченной цели. </p>
                <p>В нашей стране существует недоверие к слову «кооператив» и ко всем подобным проектам, призывающим к объединению и обещающим улучшение благосостояния народа. Такая ситуация сложилась из-за действий определённых лиц, сыгравших на экономической неграмотности нашего населения в постсоветское время и его непонимании, что из себя представляет «рыночная экономика». И несмотря на то, что история кооперации в России, со статьёй о которой мы предлагаем ознакомиться на нашем сайте (ссылка), говорит об эффективности такого вида экономической деятельности, люди предпочитают обходить стороной организации кооперативного типа, тем самым самостоятельно отбирая у себя возможность превратить свою жизнь именно в то, что снится в тех прекрасных, кажущихся сказочными, снах, после которых ну так не хочется просыпаться. </p>
                <p>Мы хорошо понимаем, почему люди с такой осторожностью и недоверием относятся к тому, что мы предлагаем, ведь все мы жители одной страны и знаем, что в ней происходило и происходит на данный момент, а некоторые из близких нам людей сами становились жертвами мошенников, желающих сколотить состояние за счёт доверия честных граждан, которые просто хотят улучшить свою жизнь, а также сделать вклад в будущее своих детей. Именно это добавляет ещё большее рвения и энтузиазма доказать, что честное объединение может улучшить вашу жизнь настолько, что вы забудете, что ещё недавно выбирали продукты в гипермаркетах по красным ценникам, занимали деньги у соседей, чтобы дожить до зарплаты, годами откладывали на недельный отпуск в Турции и мечтать только могли о квалифицированной медицинской помощи и качественных лекарствах.</p>
                <p>Мы хотим изменить представление людей о кооперативах, именно поэтому, вы всегда можете посмотреть открытую статистику в популярных социальных сетях (ссылки). Там же находится учредительная документация и контакты администрации. Очень важно, что мы не прячем своих лиц и имён, и всегда готовы ответить за деятельность кооператива.</p>
                <p>На данный момент мы начинаем реализацию первых двух программ - «Жилье ВСЕМ-10» (ссылка) и «Авто ГИМ» (ссылка). Все деньги пайщиков, вложенные в программы, используются строго по своему назначению – покупка жилья или автомобиля соответственно. Мы не обещаем «преумножить» ваш вклад, не используем ваши деньги для инвестиций, но и не требуем от вас создания сети клиентов и построения тех самых злосчастных пирамид. </p>
                <p>Программы «Жилье ВСЕМ-10» и «Авто ГИМ» Созданы для улучшения жилищных и материальных условий ВСЕХ пайщиков кооператива. Суть программ заключается в том, чтобы совместными усилиями обеспечить всех пайщиков кооператива, присоединившихся к соответствующей Программе, жильем и автомобилями. При этом никаких справок, поручителей и первоначальных взносов нет!  </p>
                <p>Программы реализуются независимо друг от друга. Достигнув определенного количества пайщиков ПК открывает ПОТОК № 1 программы «авто» или «жилье». Количество потоков не ограничено, но между собой эти потоки пересекаться не будут. У каждого потока будет отдельный банковский счет и своя команда обслуживающего персонала. Такое разделение потоков гарантирует каждому пайщику получение жилья и исключает какую-либо «пирамидность».  </p>
                <p>При желании каждый пайщик может участвовать в неограниченном количестве потоков и программ, так же принимать неограниченное участия в одном потоке (так, например Иванов И.И. может принимать участие в одном потоке по Программе "Жилье" 3-5-8 раз, сколько позволяет финансовое состояние пайщика).</p>
                <p>Мы гарантируем 100%, что все члены ПК "Мой Дом ГИМ", присоединившиеся к соответствующим Программам, получат собственное жилье и автомобили.</p>
                <p>Так же, мы осознаём, что в какой-либо момент времени у пайщика может сложиться такая жизненная ситуация, что он больше не сможет продолжать участие в программах для получения жилья и авто. На такой случай у нас предусмотрена возможность в любое время, без предоставления справок и объяснения причин, покинуть наш ПК. В таком случае человек свободно, без каких-либо трудностей и бумажной волокиты, может выйти из кооператива с полным возвратом всех средств, вложенных в программы. </p>
                <p>Ежемесячно, жилье и автомобили будут выданы совершенно случайным пайщикам из соответствующего потока, участвующим в программе, путем случайного выбора (вне зависимости от размера вклада «значимости» лица или каких-либо еще обстоятельств – абсолютный рандом). Выбирать будет добровольно формирующаяся комиссия из числа участников ПК, членом которой может стать абсолютно любой пайщик.  Мы решили, что так будет правильно и исключит какие-либо возможности обмана. Единственное исключение сделано для первых пятисот пайщиков по программе «жилье» и ста пятидесяти по программе «авто». Данная категория пайщиков, вступившие в кооператив первыми, гарантированно получат жилье в течение 5 лет, а автомобили в течение 2 лет! Когда ПК Мой Дом ГИМ крепко встанет на ноги, мы будем реализовывать и другие программы, мы открыты для предложений.</p>
                <p>Вновь вступившим в кооператив мы дарим подарки:
